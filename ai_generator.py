def save_feedback(user_id, username, text, feedback_type="bug"):
    """Сохраняет обратную связь в файл и отправляет админу"""
    try:
        os.makedirs('feedback', exist_ok=True)
        timestamp = datetime.now().strftime('%Y-%m-%d %H:%M:%S')
        filename = f"feedback/feedback_{datetime.now().strftime('%Y-%m-%d')}.txt"
        
        with open(filename, 'a', encoding='utf-8') as f:
            f.write(f"\n{'='*50}\n")
            f.write(f"📅 {timestamp}\n")
            f.write(f"👤 {username} (ID: {user_id})\n")
            f.write(f"📌 Тип: {feedback_type}\n")
            f.write(f"📝 Текст: {text}\n")
            f.write(f"{'='*50}\n")
        
        print(f"💾 Сохранено в файл: {filename}")
        
        send_feedback_to_admin(user_id, username, text, feedback_type)
        return True
    except Exception as e:
        print(f"❌ Ошибка сохранения: {e}")
        return False