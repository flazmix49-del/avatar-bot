def get_compatibility(name1="", name2=""):
    if not name1 or not name2:
        return "❌ Напиши два имени через пробел."
    
    name1 = name1.strip().lower()
    name2 = name2.strip().lower()
    
    letters = "абвгдеёжзийклмнопрстуфхцчшщъыьэюяabcdefghijklmnopqrstuvwxyz"
    
    sum1 = sum(letters.find(c) + 1 for c in name1 if c in letters)
    sum2 = sum(letters.find(c) + 1 for c in name2 if c in letters)
    
    if sum1 == 0 or sum2 == 0:
        return "❌ Имена должны содержать буквы!"
    
    base_percent = (sum1 + sum2) % 100
    random.seed(name1 + name2)
    bonus = random.randint(0, 20)
    percent = min(98, max(2, base_percent + bonus))
    
    if percent >= 80:
        msg = "💖 Идеальная совместимость! Вы созданы друг для друга!"
    elif percent >= 60:
        msg = "💕 Хорошая совместимость! У вас есть потенциал!"
    elif percent >= 40:
        msg = "💛 Средняя совместимость. Может что-то и выйдет!"
    elif percent >= 20:
        msg = "🧡 Низкая совместимость. Придётся постараться!"
    else:
        msg = "💔 Очень низкая совместимость. Лучше остаться друзьями!"
    
    return f"💞 **Совместимость имён**\n\n👤 **{name1.capitalize()}** ❤️ **{name2.capitalize()}**\n\n🔥 **{percent}%**\n\n{msg}"

