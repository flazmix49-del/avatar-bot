def convert_currency(amount=1, from_currency="usd", to_currency="rub"):
    try:
        url = f"https://api.exchangerate-api.com/v4/latest/{from_currency.upper()}"
        response = requests.get(url, timeout=10)
        if response.status_code == 200:
            data = response.json()
            rate = data['rates'].get(to_currency.upper())
            if rate:
                result = amount * rate
                return f"💱 **{amount} {from_currency.upper()}** = **{result:.2f} {to_currency.upper()}**\n\nКурс: 1 {from_currency.upper()} = {rate:.2f} {to_currency.upper()}"
        return None
    except:
        return None

